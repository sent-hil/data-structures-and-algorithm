const canPartition = (arr) => {
  const avg = arr.reduce((acc, v) => acc + v) / 2

  // arr will only contain integers, therefore can't have decimal
  if (Math.floor(avg) !== avg) return false

  const half = subset(arr, avg)

  if (half === null) return false

  for (const item of half) {
    const i = arr.indexOf(item)
    if (i !== -1) { arr.splice(i, 1) }
  }

  if (arr.reduce((acc, v) => acc + v) !== avg) return false

  return true
}

const subset = (arr, sum, memo = {}) => {
  for (let i = 0; i < arr.length - 1; i++) {
    let c = arr[i]
    let r = [...arr.slice(0, i), ...arr.slice(i + 1)]
    if (sum - c === 0) return [c]
    if (sum < 0) { return null }

    if (typeof memo[[c, ...r].join('')] !== 'undefined') return memo[[c, ...r].join('')]

    let k = subset(r, sum - c, memo)
    if (k !== null) {
      memo[[c, ...r].join('')] = [c, ...k]
      return [c, ...k]
    } else {
      memo[[c, ...r].join('')] = k
    }
  }

  return null
}

